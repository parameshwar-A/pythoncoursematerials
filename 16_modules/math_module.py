import math

# Ceil function is used to get ceiling of the number
print(math.ceil(2.205))
print(math.ceil(2.75))

# Floor function is used to get lowest value of the decimal.
print(math.floor(2.995))
print(math.floor(2.20))

# Isqrt method used to find integer part of the square root.
print(math.isqrt(3))

# Remainder method used to give use the reminder of the division
print(math.remainder(10,2))

# pi is just a value not a method
print(math.pi)

# sqrt function used to get us the square root 
print(math.sqrt(3))

# sin will give you the sine value of the number
print(math.sin(3))

# exp is used to do exponential value of the base number
print(math.exp(2))

# Factorial used to find factorial of the given number
# example 3: 3*2*1
print(math.factorial(3))

# perm method is used to find permutation of the given values
print(math.perm(2,0))
