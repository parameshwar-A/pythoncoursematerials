# Comparative are for comparing two or more input conditions

# In real life we compare lot of data, to learn about them.

# We compare age, data, speed, metrics, name and lot more than we think.

# To perform these comparison operations, python offers comparision operators

# Comparison operators results in boolean outputs, results true if the given comparison is positive

# Greater than (>) 
print(1 > 2)

# Greater than equal to (>=)
print(1>=1)

## Lesser than (<)
print(100<200)

# Lesser than equal to (<=)
print(100<=101)

# Equals too
print(1==1)


